#include<stdio.h>

int main()
{
	printf("문자 a : %c\n", 'a');
	printf("문자 a의 ASCII 코드 : %d\n", 'a'); // ASCII 코드 출력
	printf("문자 a의 ASCII 코드 : %x\n", 'a'); // ASCII 코드 16진수로 출력
	printf("문자 a의 ASCII 코드 : %o\n", 'a'); // ASCII 코드 8진수로 출력
	printf("문자 a의 ASCII 코드 : %c\n", 97); // ASCII 코드를 문자로 출력  

	return 0;
}
