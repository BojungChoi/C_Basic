#include<stdio.h>

int main()
{
	printf("문자 a : %c\n", 'a');
	printf("문자 a의 ASCII 코드 : %d\n", 'a'); // ASCII 코드 출력 10진수
	printf("문자 a의 ASCII 코드 : %x\n", 'a'); // ASCII 코드 출력 16진수
	printf("문자 a의 ASCII 코드 : %o\n", 'a'); // ASCII 코드 출력 8진수
	printf("문자 a의 ASCII 코드 : %c\n", 97); // ASCII 코드 출력 문자
	return 0;

}
